package com.challenge.tournament.domain;

public class League {

	private String leagueName;

	private String leagueCity;
	
	private int leagueYear;

	public String getLeagueName() {
		return leagueName;
	}

	public void setLeagueName(String leagueName) {
		this.leagueName = leagueName;
	}

	public String getLeagueCity() {
		return leagueCity;
	}

	public void setLeagueCity(String leagueCity) {
		this.leagueCity = leagueCity;
	}

	public int getLeagueYear() {
		return leagueYear;
	}

	public void setLeagueYear(int leagueYear) {
		this.leagueYear = leagueYear;
	}



}
